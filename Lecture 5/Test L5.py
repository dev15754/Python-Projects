# Search for 99 in (1,4,9,16,25) using while loop, print "not found" using else block
n=(1,4,9,16,25)
b=99
idx=0
while  idx<len(n):
    if n[idx]==b:
        print("found")
        break
    idx+=1
else:
        print("not found")